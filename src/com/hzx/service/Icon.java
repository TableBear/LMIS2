package com.hzx.service;

public class Icon
{
  int w;
  int h;
  int l;
  int t;
  int x;
  int lb;
  
  public int getW()
  {
    return this.w;
  }
  
  public Icon(int w, int h, int l, int t, int x, int lb)
  {
    this.w = w;
    this.h = h;
    this.l = l;
    this.t = t;
    this.x = x;
    this.lb = lb;
  }
  
  public void setW(int w)
  {
    this.w = w;
  }
  
  public int getH()
  {
    return this.h;
  }
  
  public void setH(int h)
  {
    this.h = h;
  }
  
  public int getL()
  {
    return this.l;
  }
  
  public void setL(int l)
  {
    this.l = l;
  }
  
  public int getT()
  {
    return this.t;
  }
  
  public void setT(int t)
  {
    this.t = t;
  }
  
  public int getX()
  {
    return this.x;
  }
  
  public void setX(int x)
  {
    this.x = x;
  }
  
  public int getLb()
  {
    return this.lb;
  }
  
  public void setLb(int lb)
  {
    this.lb = lb;
  }
}
